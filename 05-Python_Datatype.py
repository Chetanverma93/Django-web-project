# Python Data Types

"""
1. integer
2. float
3. string
4. boolean
5. complex
6. unicode

"""

# Example of integer

a = 5
print("a = ", a)
print(type(a))
print("\n")


# Example of float

a = 5.5
print("a = ", a)
print(type(a))
print("\n")


# Example of string

a = "Hello World"
print("a = ", a)
print(type(a))
print("\n")


# Another Example of string

a = "I don't like this"
print("a = ", a)
print(type(a))
print("\n")


#  More Example of string

s = 'Hello World!'

print(s)           # Prints complete string
print(s[0])        # Prints first character of the string
print(s[2:5])      # Prints characters starting from 3rd to 5th
print(s[2:])       # Prints string starting from 3rd character
print(s * 2)       # Prints string two times
print(s + "TEST")  # Prints concatenated string
print('\n')


# Example of boolean

a = True
print("a = ", a)
print(type(a))
print("\n")


# Example of complex

a = 3 + 4j
print(a)
x = a.real
y = a.imag
print("real = ", x)
print("img = ", y)
print(type(a))
print("\n")


# Example of unicode

s = u"\U0001F4A9"
print("unicode = ", s)
print("Data type :- ", type(s))
print("\n")
